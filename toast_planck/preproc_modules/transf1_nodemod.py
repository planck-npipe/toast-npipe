import numpy as np
import toast.timing as timing

t0 = 1628777624967572461 * 1e-9  # Reference time for measuring dV / dADU


class Transf1():
    """
    Operator to convert ADU-valued HFI data to Volts using the relevant
    housekeeping data.
    """

    def __init__(self):
        pass

    def expand_hsk(self, hsk_field, time):
        """
        Housekeeping fields are only sampled at the time they change.
        This function returns a densely sampled housekeeping vector.

        """
        # load the housekeeping data

        grp, obj = hsk_field.split(b'/')
        t, x = hsk[grp][obj]

        # housekeeping time stamps are in nanoseconds. Test if they
        # need to be scaled to supplied time units

        i0 = len(time) // 2

        if time[i0] < 1e10:
            # from nanoseconds to seconds
            tt = t.astype(np.float64) * 1e-9
        elif time[i0] < 1e18:
            # from nanoseconds to OBT ticks
            tt = t.astype(np.float64) * 1e-9 * 2.**16.
        else:
            tt = t

        # interpolate

        ind = np.searchsorted(tt, time)
        return x[ind - 1]

    def dVdADU(self, bolo):
        """
        Measure the ratio of Volts / ADU at t0.

        """
        imin = 0
        imax = 2 ** 24 - 1
        toi_ADU = np.array([imin, imax], dtype=np.int32)
        time = np.array([t0, t0], dtype=np.float64)
        toi_Volt = self.convert(toi_ADU, time, bolo)
        return (toi_Volt[1] - toi_Volt[0]) / (imax - imin)

    def convert(self, toi, time, bolo, unconvert=False):
        """
        Apply the transf1_nodemod tranfer function to the input TOI
        by interpolating the necessary housekeeping fields to the
        provided timestamps

        Args:
            toi (ndarray):  Input ADU-valued data (unconvert=False)
                or Volt data (unconvert=True)
            time (float):  timestamps in TAI seconds.
            bolo (str):  bolometer identifier such as "00_100_1a"
            unconvert (bool):  Optionally the conversion may be applied
                in inverse

        """
        params = hsk[bolo.encode()]  # python 3

        # Fixed IMO params

        GC_bc = params[b'GC_bc']
        F1_bc = params[b'F1_bc']
        HFI_REU_ETAL = params[b'HFI_REU_ETAL']
        REU_bc_offset = params[b'REU_bc_offset']

        # Housekeeping fields

        gamp = self.expand_hsk(params[b'gamp'], time)
        nsamp = self.expand_hsk(params[b'nsamp'], time)
        nblanck = self.expand_hsk(params[b'nblanck'], time)

        # interpret the nsamp codes

        nsamp[nsamp == 0] = 45
        nsamp[nsamp == 1] = 40
        nsamp[nsamp == 2] = 36
        nsamp[nsamp == 3] = 45

        # apply the function

        if unconvert:
            new_toi = (
                toi.astype(np.float64) * GC_bc[gamp] * F1_bc
                / GC_bc[HFI_REU_ETAL] + REU_bc_offset) \
                * (nsamp - nblanck).astype(np.float64)
        else:
            new_toi = (
                toi.astype(np.float64) / (nsamp - nblanck).astype(np.float64)
                - REU_bc_offset) \
                * GC_bc[HFI_REU_ETAL] / F1_bc / GC_bc[gamp]

        # and return
        return new_toi

